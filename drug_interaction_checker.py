def _normalize(drug_name: str) -> str:
    """Нормализует название препарата к каноническому МНН."""
    s = drug_name.strip().lower()
    # Убрать дозировки: "метформин 500 мг" → "метформин"
    s = re.sub(r'\s+\d+[\d,\.]*\s*(мг|г|мл|ме|ед\.?|мкг|mg|g|ml|iu)?$', '', s).strip()
    return _SYNONYMS.get(s, s)


@dataclass
class Interaction:
    drug1: str
    drug2: str
    severity: str
    description: str


@dataclass
class CheckResult:
    drugs: List[str]
    interactions: List[Interaction] = field(default_factory=list)
    critical_count: int = 0
    high_count: int = 0
    moderate_count: int = 0
    low_count: int = 0

    def has_issues(self) -> bool:
        return bool(self.interactions)


def check_interactions(drug_names: List[str]) -> CheckResult:
    """
    Проверяет список препаратов на взаимодействия.
    Возвращает CheckResult с найденными парами.
    """
    normalized = [_normalize(d) for d in drug_names]
    result = CheckResult(drugs=drug_names)

    seen_pairs = set()

    for i, d1 in enumerate(normalized):
        for j, d2 in enumerate(normalized):
            if j <= i:
                continue
            pair_key = (min(d1, d2), max(d1, d2))
            if pair_key in seen_pairs:
                continue

            # Прямое совпадение
            for drug_a, drug_b, severity, desc in _INTERACTIONS:
                # Нечёткое сопоставление: препарат может быть подстрокой
                match_a = (d1 == drug_a or d1.startswith(drug_a) or drug_a in d1)
                match_b = (d2 == drug_b or d2.startswith(drug_b) or drug_b in d2)
                match_ba = (d2 == drug_a or d2.startswith(drug_a) or drug_a in d2)
                match_ab = (d1 == drug_b or d1.startswith(drug_b) or drug_b in d1)

                if (match_a and match_b) or (match_ba and match_ab):
                    seen_pairs.add(pair_key)
                    inter = Interaction(
                        drug1=drug_names[i],
                        drug2=drug_names[j],
                        severity=severity,
                        description=desc,
                    )
                    result.interactions.append(inter)
                    if severity == CRITICAL:
                        result.critical_count += 1
                    elif severity == HIGH:
                        result.high_count += 1
                    elif severity == MODERATE:
                        result.moderate_count += 1
                    else:
                        result.low_count += 1
                    break

    return result


def format_report(result: CheckResult, patient_name: str = "") -> str:
    """Форматирует CheckResult в читаемый текст для Telegram / CLI."""
    lines = []
    header = f"🔍 Проверка взаимодействий препаратов"
    if patient_name:
        header += f" — {patient_name}"
    lines.append(header)
    lines.append(f"Препараты ({len(result.drugs)}): {', '.join(result.drugs)}")
    lines.append("")

    if not result.interactions:
        lines.append("✅ Значимых взаимодействий не обнаружено (в локальной базе).")
        lines.append("Рекомендуется дополнительная проверка по Drugs.com или UpToDate.")
        return "\n".join(lines)

    lines.append(f"Найдено взаимодействий: {len(result.interactions)}")
    if result.critical_count:
        lines.append(f"  {CRITICAL}: {result.critical_count}")
    if result.high_count:
        lines.append(f"  {HIGH}: {result.high_count}")
    if result.moderate_count:
        lines.append(f"  {MODERATE}: {result.moderate_count}")
    if result.low_count:
        lines.append(f"  {LOW}: {result.low_count}")
    lines.append("")

    # Сортировка: сначала критические
    severity_order = {CRITICAL: 0, HIGH: 1, MODERATE: 2, LOW: 3}
    sorted_inter = sorted(result.interactions, key=lambda x: severity_order.get(x.severity, 9))

    for inter in sorted_inter:
        lines.append(f"{inter.severity}")
        lines.append(f"  {inter.drug1}  ×  {inter.drug2}")
        lines.append(f"  {inter.description}")
        lines.append("")

    lines.append("⚕️ Решение о коррекции терапии — за лечащим врачом.")
    return "\n".join(lines)


def check_patient_medications(patient_id: int) -> Optional[CheckResult]:
    """
    Загружает активные препараты пациента из БД и проверяет взаимодействия.
    """
    try:
        from db import get_medications
        rows = get_medications(patient_id, status="active")
        if not rows:
            return None
        drug_names = [r["drug_name"] for r in rows]
        return check_interactions(drug_names)
    except Exception as e:
        import logging
        logging.getLogger(__name__).warning(f"drug_interaction_checker: {e}")
        return None


