    for ch_num, ch_para in reversed(chapter_paragraphs):
        key = str(ch_num)
        if key not in VIGNETTES:
            continue
        texts = VIGNETTES[key]
        elements = [make_elem('i', t) for t in texts]
        insert_after(ch_para._element, elements)
        inserted += 1
    print(f"Вставлено вигнеток: {inserted}")

    # 4. Замена эпилога
    print("Заменяю эпилог...")
    body = doc.element.body

    # Находим элемент с текстом "ЭПИЛОГ"
    epilog_elem = None
    for p in doc.paragraphs:
        if p.text.strip() == "ЭПИЛОГ":
            epilog_elem = p._element
            break

    if epilog_elem is None:
        print("  ВНИМАНИЕ: заглушка эпилога не найдена, добавляю в конец.")
    else:
        # Удаляем всё от ЭПИЛОГ до конца документа
        found = False
        to_remove = []
        for child in body:
            if found:
                to_remove.append(child)
            elif child is epilog_elem:
                found = True
                to_remove.append(child)

        for elem in to_remove:
            try:
                body.remove(elem)
            except Exception:
                pass
        print(f"  Удалено {len(to_remove)} элементов старого эпилога.")

    # Добавляем новый эпилог
    h1 = make_elem('h2', EPILOG_TITLE)  # используем h2 для визуальной совместимости
    # Но лучше Heading 1:
    pPr = OxmlElement('w:pPr')
    pStyle = OxmlElement('w:pStyle')
    pStyle.set(qn('w:val'), 'Heading1')
    pPr.clear()
    pPr.append(pStyle)
    h1.insert(0, pPr)
    body.append(h1)

    for tag, text in EPILOG:
        if tag == 'h2':
            elem = make_elem('h2', text)
        elif tag == 'i':
            elem = make_elem('i', text, italic=True)
        elif tag == 'sep':
            elem = make_elem('sep', text, center=True)
        else:
            elem = make_elem('p', text)
        body.append(elem)

    print("Эпилог вставлен.")

    # 5. Сохраняем
    print(f"Сохраняю в '{OUTPUT}'...")
    doc.save(OUTPUT)
    print("Готово!")

    # Быстрая проверка
    doc2 = docx.Document(OUTPUT)
