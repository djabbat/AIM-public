    doc = docx.Document(INPUT)

    # 1. Исправления противоречий
    print("Применяю исправления противоречий...")
    for old_text, new_text in FIXES:
        for p in doc.paragraphs:
            if old_text in p.text:
                if new_text == "":
                    # Очищаем абзац
                    for r in p.runs:
                        r.text = ""
                    # Удаляем все run-элементы из XML
                    for child in list(p._element):
                        if child.tag.endswith('}r'):
                            p._element.remove(child)
                    print(f"  Удалён: {old_text[:60]}...")
                else:
                    # Заменяем текст
                    for r in p.runs:
                        r.text = ""
                    if p.runs:
                        p.runs[0].text = new_text
                    else:
                        p.add_run(new_text)
                    print(f"  Заменён: {old_text[:60]}...")
                break

