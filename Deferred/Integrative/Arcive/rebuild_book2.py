def make_elem(tag, text, italic=False, center=False):
    p = OxmlElement('w:p')
    if tag == 'h2':
        pPr = OxmlElement('w:pPr')
        pStyle = OxmlElement('w:pStyle')
        pStyle.set(qn('w:val'), 'Heading2')
        pPr.append(pStyle)
        p.append(pPr)
        r = OxmlElement('w:r')
        t = OxmlElement('w:t'); t.text = text
        r.append(t); p.append(r)
        return p

    if center or tag == 'sep':
        pPr = OxmlElement('w:pPr')
        jc = OxmlElement('w:jc'); jc.set(qn('w:val'), 'center')
        pPr.append(jc); p.append(pPr)

    r = OxmlElement('w:r')
    rPr = OxmlElement('w:rPr')
    if italic or tag == 'i':
        i_elem = OxmlElement('w:i'); rPr.append(i_elem)
        iCs = OxmlElement('w:iCs'); rPr.append(iCs)
    r.append(rPr)
    t = OxmlElement('w:t'); t.text = text
    t.set(qn('xml:space'), 'preserve')
    r.append(t); p.append(r)
    return p


def insert_after(ref_elem, elements):
    """Вставляет список элементов после ref_elem в прямом порядке."""
    cur = ref_elem
    for e in elements:
        cur.addnext(e)
        cur = e


