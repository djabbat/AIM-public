def make_paragraph_element(text, style_hint="italic"):
    """Создаёт XML-элемент абзаца с нужным форматированием."""
    new_p = OxmlElement('w:p')

    if text == "*":
        # Разделитель — горизонтальная линия через текст
        pPr = OxmlElement('w:pPr')
        jc = OxmlElement('w:jc'); jc.set(qn('w:val'), 'center')
        pPr.append(jc)
        new_p.append(pPr)
        r = OxmlElement('w:r')
        t = OxmlElement('w:t'); t.text = "* * *"
        r.append(t)
        new_p.append(r)
        return new_p

    r = OxmlElement('w:r')
    rPr = OxmlElement('w:rPr')
    if style_hint == "italic":
        i = OxmlElement('w:i'); rPr.append(i)
        iCs = OxmlElement('w:iCs'); rPr.append(iCs)
    r.append(rPr)
    t = OxmlElement('w:t')
    t.text = text
    t.set(qn('xml:space'), 'preserve')
    r.append(t)
    new_p.append(r)
    return new_p


def make_heading_element(text, level=2):
    """Создаёт XML-элемент заголовка."""
    new_p = OxmlElement('w:p')
    pPr = OxmlElement('w:pPr')
    pStyle = OxmlElement('w:pStyle')
    pStyle.set(qn('w:val'), f'Heading{level}')
    pPr.append(pStyle)
    new_p.append(pPr)
    r = OxmlElement('w:r')
    t = OxmlElement('w:t'); t.text = text
    r.append(t)
    new_p.append(r)
    return new_p


def insert_elements_after(ref_elem, elements):
    """Вставляет список XML-элементов после ref_elem в порядке списка."""
    current = ref_elem
    for elem in elements:
        current.addnext(elem)
        current = elem


